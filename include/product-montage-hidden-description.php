<span class="description__title">Установка скрытой двери: </span> <br>
<br>
<div class="Ub_1">
    <img alt="11_07.png" src="/upload/medialibrary/b03/b0387e50b3d5c0bd3de1172563db74eb.png" title="11_07.png">
    <h4>
        Внимание! </h4>
    <p>
        Коробка монтируется до проведения финишной отделки стен. Зазор между полотном и коробкой должен составлять 3 мм. Перед началом работ убедитесь в наличии необходимого инструмента, комплектующих, проверьте соответствие размеров полотна и проёма.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_1_03.jpg" src="/upload/medialibrary/c76/c76ad2ad2d7176a3bcc908f505a8b4b7.jpg" title="Ub_1_03.jpg">
    <p>
        1. Верхняя часть проёма должна располагаться горизонтально и паралельно полу. Боковые стороны проёма должны быть вертикальными и паралельными.
    </p>
    <p style="margin-top: 80px;">
        2. Стойки коробки поставляются с торцами, торцованными с одной стороны под углом 45° и под углом 90° с другой. Поперечный брусок коробки торцован с двух сторон под углом 45°.
    </p>
</div>
<div class="Ub_2">
    <img alt="ub_2_03.jpg" src="/upload/medialibrary/618/618edd19a8d17faaa0360fb5999a0cb9.jpg" title="ub_2_03.jpg">
    <p style="margin-top: 50px;">
        3. Разместите детали коробки на чистой ровной поверхности, соедините стойки и поперечный брусок, закрепите саморезами всю конструкцию.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_3_03.jpg" src="/upload/medialibrary/604/604674b8321621ed5064efa653abf655.jpg" title="Ub_3_03.jpg">
    <p style="margin-top: 50px;">
        4. Собранную раму поместите в проём. Выровняйте по уровню в горизонтальной и вертикальной плоскостях. Закрепите саморезами. Внутреннее растояние коробки должно быть одинаково по всей высоте и равняться ширине полотна + 6мм.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_4_03.jpg" src="/upload/medialibrary/2da/2dac6f59392b5bfd5b59c38fa10a374d.jpg" title="Ub_4_03.jpg">
    <p style="margin-top: 50px;">
        5. Заполните зазор между коробкой и проёмом монтажной пеной, дождитесь полного высыхания пены перед финишной отделкой стен.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_5_03.jpg" src="/upload/medialibrary/0c3/0c3f3e29cddf8cefc6aad7b986ff8d43.jpg" title="Ub_5_03.jpg">
    <p style="margin-top: 50px;">
        6. Выровняйте при помощи шпатлевки плоскость стены и дверной коробки.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_6_03.jpg" src="/upload/medialibrary/aaa/aaa3dca1277b816bf8e9818dda993f43.jpg" title="Ub_6_03.jpg">
    <p style="margin-top: 50px;">
        7. Нанесите декоративное покрытие (краска, обои, декоративная штукатурка), предварительно подготовив поверхность.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_7_03.jpg" src="/upload/medialibrary/553/553d30daf7049d5862a1e5fe121cd917.jpg" title="Ub_7_03.jpg">
    <p style="margin-top: 50px;">
        8. Закрепите створчатую часть петли в посадочном месте в полотне, повесьте полотно в имеющиеся посдочные места в коробке и закрепите. Отрегулируйте зазоры и притвор полотна с помощью регулировочных винтов на петлях.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_8_03.jpg" src="/upload/medialibrary/86f/86f63db1c2d7784110429c0e0f510fc9.jpg" title="Ub_8_03.jpg">
    <p style="margin-top: 50px;">
        9. Нанесите декоративное покрытие на дверное полотно (краска, обои, декоративная штукатурка), предварительно подготовив поверхность.
    </p>
</div>
<div class="Ub_2">
    <img alt="Ub_9_03.jpg" src="/upload/medialibrary/339/3396eb8378522296999a328a8431e6c4.jpg" title="Ub_9_03.jpg">
    <p style="margin-top: 50px;">
        10. Установите замок и ручку в полотно. На петлю установите декоративные накладки.
    </p>
</div>
