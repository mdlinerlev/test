<span class="description__title">Установка обычной двери: </span> <br>
 <br>
<div class="description__text">
	 Установка только после окончательной внутренней отделки помещения Монтаж межкомнатных дверей производится после окончательной внутренней отделки всего помещения, включая напольное покрытие. Далее необходимо тщательно очистить, выровнять штукатуркой и зашпаклевать откосы межкомнатного проема. Подготовленный дверной проем должен иметь технологический зазор для последующей регулировки короба и заполнения пространства монтажной пеной.
</div>
 <br>
<div class="description__subtitle">
	 Шаг1. Монтаж дверного блока
</div>
<div class="description__text">
	 Монтаж производится в определенной последовательности и включает в себя следующие этапы: сборка и установка дверной коробки, монтаж дверного полотна, установка наличников.
</div>
 <br>
<div class="description__subtitle">
	 Шаг2. Сборка и установка дверной коробки
</div>
<div class="description__text">
	 Производится на ровной поверхности. В домашних условиях это можно сделать на полу. Установка собранной дверной коробки в стеновой проем производится при помощи распорок и клиньев, которые фиксируют короб и одновременно дают возможность корректировки всей конструкции.
</div>
 <br>
<div class="description__subtitle">
	 Шаг3. Монтаж дверного полотна
</div>
<div class="description__text">
	 Подготовка подразумевает врезку петель, замка и установку ручек. Стандартным и удобным расположением дверной ручки считается расстояние в пределах 0,9 – 1,2 м от пола.
</div>
 <br>
<div class="description__subtitle">
	 Шаг4. Установка наличников
</div>
<div class="description__text">
	 Установка дверных наличников на двери подразумевает подрезку мест соединения отдельных частей комплекта может выполняться двумя принципиально разными способами – как говорят профессионалы, с подрезкой под 45° (наиболее распрорстраненный) или под 90°.. <br>
